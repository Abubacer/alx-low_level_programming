#include "_putchar.c"

/**
* main - entry point
* this is a program that prints _putchar
* followed by a new line.
* Return: 0 (Success)
*/


int main(void)
{
	int i;

	for (i = 0 ; i < 1 ; i++)
	{
		putchar('_');
		putchar('p');
		putchar('u');
		putchar('t');
		putchar('c');
		putchar('h');
		putchar('a');
		putchar('r');
	}
	putchar('\n');
	return (0);
}
