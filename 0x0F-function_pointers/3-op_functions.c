#include "3-calc.h"

/**
 * op_add - a function that compute the sum of two numbers
 * @a: the first number
 * @b: the seconde number
 *
 * Return: the sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - a function that compute the subdivision of two numbers
 * @a: the first number
 * @b: the seconde number
 *
 * Return: the difference of a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - a function that compute the product of two numbers
 * @a: the first number
 * @b: the seconde number
 *
 * Return: the product of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - a function that compute the division of two numbers
 * @a: the first number
 * @b: the seconde number
 *
 * Return: the division of a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - a function that compute the remainder of two numbers
 * @a: the first number
 * @b: the seconde number
 *
 * Return: the remainder of a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
