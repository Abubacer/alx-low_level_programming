
/**
 * add - a function that sums two numbers.
 * @a: first number.
 * @b: seconde number.
 *
 * Return: the result.
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - a function that subtracts two numbers.
 * @a: first number.
 * @b: seconde number.
 *
 * Return: the result.
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - a function that multiplies two numbers.
 * @a: first number.
 * @b: seconde number.
 *
 * Return: the result.
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - a function that divides two numbers.
 * @a: first number.
 * @b: seconde number.
 *
 * Return: the result.
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - a function that finds modulus of two numbers.
 * @a: first number.
 * @b: seconde number.
 *
 * Return: the result.
 */

int mod(int a, int b)
{
	return (a % b);
}



